    allowedDecisionTaskIds: ["t-20260710-001", "t-20260710-002"],
    decisionGroups: [{ meeting: "meetings/2026-07-10/Planning.md", taskIds: ["t-20260710-001", "t-20260710-002"], taskTitles: ["Package feedback for Derek on the drill-through branch before it can ship", "Name the implementation owner"] }],
