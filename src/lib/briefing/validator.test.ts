test("recommendation placement accepts supplied IDs only inside Library on token-only lines", () => {
  const episode = "rec-20260710052000-01-ab12cd34";
  const md = validDaily().replace(
    "- One proposal awaiting your verdict.",
    `- \`rec:${episode}\`\n- One proposal awaiting your verdict.`,
  );
  const valid = validateBriefing(md, "daily", { allowedRecommendationEpisodeIds: [episode] });
  assert.ok(!valid.failures.some((failure) => /recommendation/.test(failure)), valid.failures.join(" | "));

  const invented = validateBriefing(md, "daily", { allowedRecommendationEpisodeIds: [] });
  assert.ok(invented.failures.some((failure) => failure.includes("not supplied")));

  const annotated = validateBriefing(md.replace(`- \`rec:${episode}\``, `- Read this now \`rec:${episode}\``), "daily", { allowedRecommendationEpisodeIds: [episode] });
  assert.ok(annotated.failures.some((failure) => failure.includes("token line")));

  const outside = validateBriefing(validDaily().replace(
    "- EverPro reforecast is sales-coupled.",
    `- \`rec:${episode}\`\n- EverPro reforecast is sales-coupled.`,
  ), "daily", { allowedRecommendationEpisodeIds: [episode] });
  assert.ok(outside.failures.some((failure) => failure.includes("inside Library")));
});

